import chardet

def fix_broken_korean(text):
    # 감지된 인코딩을 이용하여 텍스트를 올바르게 디코딩
    detected = chardet.detect(text)
    encoding = detected['encoding']
    
    if encoding:
        try:
            # 올바른 인코딩으로 디코딩하여 한글 복원
            fixed_text = text.decode(encoding).encode('utf-8')
            return fixed_text.decode('utf-8')
        except Exception as e:
            print(f"Error fixing text: {e}")
            return text.decode('utf-8', 'ignore')
    else:
        return text.decode('utf-8', 'ignore')

